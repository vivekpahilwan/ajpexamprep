def area_of_square(side):
    # Exponential operator is used to determine Raised to value
    # area of sq = Square of side
    area = side ** 2
    return area


def perimeter_of_square(side):
    # perimeter of square = side 4 times
    peri = side * 4
    return peri


print(area_of_square(8))
print(perimeter_of_square(8))
